
package com.appcorsan.ed;


public class Municipio {

    private Integer codMun;
    private String txtMun;
    private String codMunIbge;

    public Integer getCodMun() {
        return codMun;
    }

    public void setCodMun(Integer codMun) {
        this.codMun = codMun;
    }

    public String getTxtMun() {
        return txtMun;
    }

    public void setTxtMun(String txtMun) {
        this.txtMun = txtMun;
    }

    public String getCodMunIbge() {
        return codMunIbge;
    }

    public void setCodMunIbge(String codMunIbge) {
        this.codMunIbge = codMunIbge;
    }

}
