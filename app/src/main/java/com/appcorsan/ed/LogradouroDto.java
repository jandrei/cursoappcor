
package com.appcorsan.ed;


public class LogradouroDto {

    private Integer codLogradouro;
    private String tpLogradouro;
    private String nomeLogradouro;
    private String nroLogradouro;

    public Integer getCodLogradouro() {
        return codLogradouro;
    }

    public void setCodLogradouro(Integer codLogradouro) {
        this.codLogradouro = codLogradouro;
    }

    public String getTpLogradouro() {
        return tpLogradouro;
    }

    public void setTpLogradouro(String tpLogradouro) {
        this.tpLogradouro = tpLogradouro;
    }

    public String getNomeLogradouro() {
        return nomeLogradouro;
    }

    public void setNomeLogradouro(String nomeLogradouro) {
        this.nomeLogradouro = nomeLogradouro;
    }

    public String getNroLogradouro() {
        return nroLogradouro;
    }

    public void setNroLogradouro(String nroLogradouro) {
        this.nroLogradouro = nroLogradouro;
    }

}
